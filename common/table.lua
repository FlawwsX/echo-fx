function TableSize(t)
	local count = 0
	for _,_ in pairs(t) do count = count + 1 end
	return count
end

function TableReverse(t)
	local newTable = {}
	for i=#t, 1, -1 do table.insert(newTable, t[i]) end
	return newTable
end

function TableSort(t, order)
    -- collect the keys
    local keys = {}
    for k in pairs(t) do keys[#keys+1] = k end

    -- if order function given, sort by it by passing the table and keys a, b,
    -- otherwise just sort the keys 
    if order then
        table.sort(keys, function(a,b) return order(t, a, b) end)
    else
        table.sort(keys)
    end

    -- return the iterator function
    local i = 0
    return function()
        i = i + 1
        if keys[i] then
            return keys[i], t[keys[i]]
        end
    end
end

function TablePrint(t, s)
    for k, v in pairs(t) do
        local kfmt = '["' .. tostring(k) ..'"]'
        if type(k) ~= 'string' then
            kfmt = '[' .. k .. ']'
        end
        local vfmt = '"'.. tostring(v) ..'"'
        if type(v) == 'table' then
            TablePrint(v, (s or '')..kfmt)
        else
            if type(v) ~= 'string' then
                vfmt = tostring(v)
            end
            print(type(t)..(s or '')..kfmt..' = '..vfmt)
        end
    end
end

exports("TableSize", TableSize)
exports("TableReverse", TableReverse)
exports("TableSort", TableSort)
exports("TablePrint", TablePrint)